package com.ps.jsonprocessingjava;

class Student {
    public String name;
    public int age;
    public boolean isEnrolled;

    public Student() {
        
    }

    public Student(String name, int age, boolean isEnrolled) {
        this.name = name;
        this.age = age;
        this.isEnrolled = isEnrolled;
    }
}
